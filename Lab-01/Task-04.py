# Code: Harshad Number Checker
num = int(input("Enter a number to check if it is a Harshad Number: "))
digit_sum = sum(int(d) for d in str(num))

if num % digit_sum == 0:
    print(num, "is a Harshad Number.")
else:
    print(num, "is NOT a Harshad Number.")
